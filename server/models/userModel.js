import mongoose from "mongoose";

const userSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  hashedPassword: {
    type: String,
    required: true,
  },
  picture: {
    type: String,
    default: "",
  },
});

export default mongoose.model("UserModel", userSchema);
